export function bubbleSort(arr) {
  const result = [...arr];

  for (let i = 0; i < result.length - 1; i++) {
    for (let j = 1; j < result.length; j++) {
      if (result[j - 1] > result[j]) {
        [result[j - 1], result[j]] = [result[j], result[j - 1]];
      }
    }
  }

  return result;
}

// 정렬이 완료된 이후에는 반복문을 종료하지만 중복이 전혀 없는 것은 아니기 때문에 개선의 여지가 있음
export function improvedBubbleSort(arr) {
  const result = [...arr];
  let flag = 1;

  for (let i = 0; flag && i < result.length - 1; i++) {
    flag = 0;
    for (let j = 1; j < result.length; j++) {
      if (result[j - 1] > result[j]) {
        [result[j - 1], result[j]] = [result[j], result[j - 1]];
        flag++;
      }
    }
  }

  return result;
}

